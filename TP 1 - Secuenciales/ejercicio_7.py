numero_1 = int(input("Ingrese un número distinto de cero: "))
numero_2 = int(input("Ingrese otro número distinto de cero: "))

suma = numero_1 + numero_2

resta = numero_1 - numero_2

multi = numero_1 * numero_2

div = numero_1 / numero_2

print(f"""
{numero_1} + {numero_2} = {suma}
{numero_1} - {numero_2} = {resta}
{numero_1} * {numero_2} = {multi}
{numero_1} / {numero_2} = {div}
""")